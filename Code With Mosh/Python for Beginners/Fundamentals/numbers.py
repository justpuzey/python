import math  # Imported for MATH module below

print(10 + 3)  # Addition
print(10 - 3)  # Subtraction
print(10 * 3)  # Multiplication
print(10 / 3)  # Division with result as float
print(10 // 3)  # Division with result as an integer
print(10 % 3)  # Modulus displays only remainder of division
print(10 ** 3)  # Exponent (10 to the 3rd power)


# standard incrementation
x = 10 + 3
print(x)

# augmented assignment operator
y = 10
y += 4
print(y)

# WORKING WITH NUMBERS
print("\n working with numbers")
print(round(2.9))
print(abs(-2.9))

# MATH OPERATOR (Imported at top of document)
print("\n using math module")
print(math.ceil(2.2))
